<?php

$numero = 15.765;

// Redondea un numero a numeros mayores .5
       // menores .1 a .4
echo round($numero);
echo "<br />";

// Un parametro para que muestre los decimales
echo round($numero, 1);
echo "<br />";

// Generar un numero aleatorio entre dos nuermos
echo rand(1,10);
echo "<br/>";

// Redondea un numeros para arriva
echo ceil($numero);
echo "<br />";

// constante por defecto de PI
echo M_PI;


